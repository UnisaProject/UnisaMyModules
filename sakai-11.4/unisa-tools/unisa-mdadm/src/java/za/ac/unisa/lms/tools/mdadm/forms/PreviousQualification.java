package za.ac.unisa.lms.tools.mdadm.forms;

public class PreviousQualification {
	
	private String qualification;
	private String institution;
	private String yearCompleted;
	private String accreditationSource;
	public String getQualification() {
		return qualification;
	}
	public void setQualification(String qualification) {
		this.qualification = qualification;
	}
	public String getInstitution() {
		return institution;
	}
	public void setInstitution(String institution) {
		this.institution = institution;
	}
	public String getYearCompleted() {
		return yearCompleted;
	}
	public void setYearCompleted(String yearCompleted) {
		this.yearCompleted = yearCompleted;
	}
	public String getAccreditationSource() {
		return accreditationSource;
	}
	public void setAccreditationSource(String accreditationSource) {
		this.accreditationSource = accreditationSource;
	}
	
	
}
