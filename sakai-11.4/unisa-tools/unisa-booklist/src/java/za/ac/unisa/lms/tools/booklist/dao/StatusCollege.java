package za.ac.unisa.lms.tools.booklist.dao;

public class StatusCollege {

	
	
	 private String college;
	 private int booklistOpen;
	 private int booklistSubmitted;
	 private int booklistAuthorized;
	 private int nobooklist;
	public String getCollege() {
		return college;
	}
	public void setCollege(String college) {
		this.college = college;
	}
	public int getBooklistOpen() {
		return booklistOpen;
	}
	public void setBooklistOpen(int booklistOpen) {
		this.booklistOpen = booklistOpen;
	}
	public int getBooklistSubmitted() {
		return booklistSubmitted;
	}
	public void setBooklistSubmitted(int booklistSubmitted) {
		this.booklistSubmitted = booklistSubmitted;
	}
	public int getBooklistAuthorized() {
		return booklistAuthorized;
	}
	public void setBooklistAuthorized(int booklistAuthorized) {
		this.booklistAuthorized = booklistAuthorized;
	}
	public int getNobooklist() {
		return nobooklist;
	}
	public void setNobooklist(int nobooklist) {
		this.nobooklist = nobooklist;
	}



















}
