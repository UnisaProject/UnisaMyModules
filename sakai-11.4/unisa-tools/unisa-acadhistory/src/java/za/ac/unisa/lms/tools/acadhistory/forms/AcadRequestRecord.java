package za.ac.unisa.lms.tools.acadhistory.forms;

public class AcadRequestRecord {
	
	 private String supplementFlag;
	 private String coverLetterFlag;
	 private String nqfCreditsFlag;
	 private String nqfLevelFlag;
	 private String marksFlag;
	 
	public String getSupplementFlag() {
		return supplementFlag;
	}
	public void setSupplementFlag(String supplementFlag) {
		this.supplementFlag = supplementFlag;
	}
	public String getCoverLetterFlag() {
		return coverLetterFlag;
	}
	public void setCoverLetterFlag(String coverLetterFlag) {
		this.coverLetterFlag = coverLetterFlag;
	}
	public String getNqfCreditsFlag() {
		return nqfCreditsFlag;
	}
	public void setNqfCreditsFlag(String nqfCreditsFlag) {
		this.nqfCreditsFlag = nqfCreditsFlag;
	}
	public String getNqfLevelFlag() {
		return nqfLevelFlag;
	}
	public void setNqfLevelFlag(String nqfLevelFlag) {
		this.nqfLevelFlag = nqfLevelFlag;
	}
	public String getMarksFlag() {
		return marksFlag;
	}
	public void setMarksFlag(String marksFlag) {
		this.marksFlag = marksFlag;
	}
	 
	
}
