package za.ac.unisa.lms.tools.unmarkassfollowup.forms;

public class AssignmentRecord {
	
	private String studyUnit;
	private String assNumber;
	public String getStudyUnit() {
		return studyUnit;
	}
	public void setStudyUnit(String studyUnit) {
		this.studyUnit = studyUnit;
	}
	public String getAssNumber() {
		return assNumber;
	}
	public void setAssNumber(String assNumber) {
		this.assNumber = assNumber;
	}
}
