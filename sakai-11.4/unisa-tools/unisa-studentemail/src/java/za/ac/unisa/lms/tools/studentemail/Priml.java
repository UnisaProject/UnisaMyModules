package za.ac.unisa.lms.tools.studentemail;

public class Priml {
	private String name;
	private String wtel;
	private String ftel;
	private String email;
	private String persno;
	/**
	 * @return Returns the email.
	 */
	public String getEmail() {
		return email;
	}
	/**
	 * @param email The email to set.
	 */
	public void setEmail(String email) {
		this.email = email;
	}
	/**
	 * @return Returns the ftel.
	 */
	public String getFtel() {
		return ftel;
	}
	/**
	 * @param ftel The ftel to set.
	 */
	public void setFtel(String ftel) {
		this.ftel = ftel;
	}
	/**
	 * @return Returns the name.
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name The name to set.
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return Returns the wtel.
	 */
	public String getWtel() {
		return wtel;
	}
	/**
	 * @param wtel The wtel to set.
	 */
	public void setWtel(String wtel) {
		this.wtel = wtel;
	}
	/**
	 * @return Returns the persno.
	 */
	public String getPersno() {
		return persno;
	}
	/**
	 * @param persno The persno to set.
	 */
	public void setPersno(String persno) {
		this.persno = persno;
	}
	
}
