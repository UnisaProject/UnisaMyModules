package za.ac.unisa.lms.tools.honsadmission.forms;

public class SignOffAccess {
	
	private String qualCode;	
	private String specCode;
	private String level;
	private String type;
	private String standInInd;
	
	public String getQualCode() {
		return qualCode;
	}
	public void setQualCode(String qualCode) {
		this.qualCode = qualCode;
	}
	public String getSpecCode() {
		return specCode;
	}
	public void setSpecCode(String specCode) {
		this.specCode = specCode;
	}
	public String getLevel() {
		return level;
	}
	public void setLevel(String level) {
		this.level = level;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getStandInInd() {
		return standInInd;
	}
	public void setStandInInd(String standInInd) {
		this.standInInd = standInInd;
	}
	
	

}
