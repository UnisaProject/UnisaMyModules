package za.ac.unisa.lms.tools.studentregistration.forms;

public class HistoryUnisa {

	private String histUniv;
	private String histQual;
	private String histQualDesc;
	private String histYear;
	private String histStudnr;
	private String histComplete;
	
	public String getHistUniv() {
		return histUniv;
	}
	public void setHistUniv(String histUniv) {
		this.histUniv = histUniv;
	}
	public String getHistQual() {
		return histQual;
	}
	public void setHistQual(String histQual) {
		this.histQual = histQual;
	}
	public String getHistQualDesc() {
		return histQualDesc;
	}
	public void setHistQualDesc(String histQualDesc) {
		this.histQualDesc = histQualDesc;
	}
	public String getHistYear() {
		return histYear;
	}
	public void setHistYear(String histYear) {
		this.histYear = histYear;
	}
	public String getHistStudnr() {
		return histStudnr;
	}
	public void setHistStudnr(String histStudnr) {
		this.histStudnr = histStudnr;
	}
	public String getHistComplete() {
		return histComplete;
	}
	public void setHistComplete(String histComplete) {
		this.histComplete = histComplete;
	}

}
