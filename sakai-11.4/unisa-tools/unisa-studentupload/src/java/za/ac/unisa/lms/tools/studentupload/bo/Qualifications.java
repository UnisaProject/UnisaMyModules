package za.ac.unisa.lms.tools.studentupload.bo;

import java.util.ArrayList;

public class Qualifications {
	
	private ArrayList<String> qualCodes;
	private ArrayList<String> qualDescs;
	
	private ArrayList<String> qualPrevCodes;
	private ArrayList<String> qualPrevDescs;
	
	private ArrayList<String> specCodes;
	private ArrayList<String> specDescs;
	
	public ArrayList<String> getQualCodes() {
		return qualCodes;
	}
	public void setQualCodes(ArrayList<String> qualCodes) {
		this.qualCodes = qualCodes;
	}
	public ArrayList<String> getQualDescs() {
		return qualDescs;
	}
	public void setQualDescs(ArrayList<String> qualDescs) {
		this.qualDescs = qualDescs;
	}
	
	
	public ArrayList<String> getQualPrevCodes() {
		return qualPrevCodes;
	}
	public void setQualPrevCodes(ArrayList<String> qualPrevCodes) {
		this.qualPrevCodes = qualPrevCodes;
	}
	public ArrayList<String> getQualPrevDescs() {
		return qualPrevDescs;
	}
	public void setQualPrevDescs(ArrayList<String> qualPrevDescs) {
		this.qualPrevDescs = qualPrevDescs;
	}
	
	public ArrayList<String> getSpecCodes() {
		return specCodes;
	}
	public void setSpecCodes(ArrayList<String> specCodes) {
		this.specCodes = specCodes;
	}
	public ArrayList<String> getSpecDescs() {
		return specDescs;
	}
	public void setSpecDescs(ArrayList<String> specDescs) {
		this.specDescs = specDescs;
	}
	
	

}
