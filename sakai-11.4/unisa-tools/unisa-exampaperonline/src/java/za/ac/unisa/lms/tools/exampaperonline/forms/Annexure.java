package za.ac.unisa.lms.tools.exampaperonline.forms;

public class Annexure {
	private String position;
	private String code;
	private String description;
	private String responsibility;
	private String averagePerStudent;
	
	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getResponsibility() {
		return responsibility;
	}
	public void setResponsibility(String responsibility) {
		this.responsibility = responsibility;
	}
	public String getAveragePerStudent() {
		return averagePerStudent;
	}
	public void setAveragePerStudent(String averagePerStudent) {
		this.averagePerStudent = averagePerStudent;
	}
	
	
	
}
