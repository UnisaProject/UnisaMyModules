package org.sakaiproject.evaluation.model;

import java.util.Date;

/**
 * This represents an adhoc group of users
 * 
 * @author Aaron Zeckoski (aaron@caret.cam.ac.uk)
 */
public class EvalAdhocGroup implements java.io.Serializable {

    private static final long serialVersionUID = 1L;

    public final static String ADHOC_ID_PREFIX = "adhoc-group:";

    /**
     * Get the id from the adhoc evalGroupId, return null if the id cannot be found
     * 
     * @param evalGroupId
     *           the unique id for this eval group
     * @return the persistent id
     */
    public static Long getIdFromAdhocEvalGroupId(String evalGroupId) {
        Long id = null;
        int location = evalGroupId.indexOf(ADHOC_ID_PREFIX);
        if (location != -1) {
            String idString = evalGroupId.substring(location + ADHOC_ID_PREFIX.length());
            id = new Long(idString);
        }
        return id;
    }

    /**
     * @return the unique eval group ID for this adhoc group (will be null if the id is null), this
     *         is generated by adding the {@link #ADHOC_ID_PREFIX} ({@value #ADHOC_ID_PREFIX}) in
     *         front of the persistent id
     */
    public String getEvalGroupId() {
        if (id != null) {
            return ADHOC_ID_PREFIX + id;
        } else {
            return null;
        }
    }

    // Fields

    protected Long id;

    protected Date lastModified;

    /**
     * The creator/owner of this adhoc group
     */
    protected String owner;

    protected String title;

    /**
     * The internal userIds of the participants in this group
     */
    protected String[] participantIds;

    /**
     * The internal userIds of the evaluatees in this group
     */
    protected String[] evaluateeIds;

    // Constructors

    /** default constructor */
    public EvalAdhocGroup() {
    }

    /**
     * Minimal constructor
     * 
     * @param owner
     * @param title
     */
    public EvalAdhocGroup(String owner, String title) {
        this(owner, title, null, null);
    }

    /**
     * Full constructor
     * 
     * @param owner
     * @param title
     * @param participantIds
     * @param evaluateeIds
     */
    public EvalAdhocGroup(String owner, String title, String[] participantIds, String[] evaluateeIds) {
        this.lastModified = new Date();
        this.owner = owner;
        this.title = title;
        this.participantIds = participantIds;
        this.evaluateeIds = evaluateeIds;
    }

    // GETTERS and SETTERS

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Date getLastModified() {
        return lastModified;
    }

    public void setLastModified(Date lastModified) {
        this.lastModified = lastModified;
    }

    public String getOwner() {
        return owner;
    }

    public void setOwner(String owner) {
        this.owner = owner;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String[] getParticipantIds() {
        return participantIds;
    }

    public void setParticipantIds(String[] participantIds) {
        this.participantIds = participantIds;
    }

    public String[] getEvaluateeIds() {
        return evaluateeIds;
    }

    public void setEvaluateeIds(String[] evaluateeIds) {
        this.evaluateeIds = evaluateeIds;
    }

}
