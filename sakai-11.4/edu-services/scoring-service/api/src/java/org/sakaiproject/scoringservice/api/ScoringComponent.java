package org.sakaiproject.scoringservice.api;

/**
 * A ScoringComponent is an optional component that a ScoringAgent might have.
 * It provides an id of some entity that might be used to help produce the score
 * for a specific gradebook item.
 *
 * For example, in the case of iRubric, there is a rubric associated with each gradebook item.
 * The associated rubric is the ScoringComponent
 *
 */
public class ScoringComponent {
    private String id;
    private String name;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
